#include "semantics.h"
#include <cstdio>
#include <cstdlib>

extern unsigned char tr_buf[], lr_buf[];

int main(int argc, char **argv)
{
    if (argc < 2)
    {
        printf("Error: no input files.");
        return 0;
    }
    FILE *fp = fopen(argv[1], "rb");
    if (fp == NULL)
    {
        printf("Error: can't locate file: %s.", argv[1]);
        return 0;
    }
    fseek(fp, 0, SEEK_END);
    int len, wlen;
    len = ftell(fp);
    rewind(fp);
    char *buf = (char *)malloc(sizeof(char) * (len + 1));
    fread(buf, 1, len, fp);
    buf[len] = 0;
    wlen = mbstowcs(NULL, buf, len);
    wchar_t *wbuf = (wchar_t *)malloc(sizeof(wchar_t) * (len + 1));
    mbstowcs(wbuf, buf, len);
    wbuf[wlen] = L'\0';
    fclose(fp);
    transition_table tr_t(tr_buf);
    lr1_parsing_table lr_t(lr_buf);
    list<token_info> tk;
    bool suc = tr_t.token_stream(wbuf, tk);
    suc &= lr_t.postfixtrans(tk, f_list, ft_list);
    printf(suc ? "true\n" : "false\n");
    return 0;
}

